static const uint8_t * font_glyph_unkown(uint32_t code)
{
	static uint8_t glyph[32];
	int idx, i;

	idx = (code >> 12) & 0xf;
	for(i = 0; i < 8; i++)
	{
		if(i == 0)
			glyph[0 + i * 2] = 0xff;
		else
			glyph[0 + i * 2] = (font_glyph_unkown_0000_000f[idx][i] << 1) | 0x80;
	}

	idx = (code >> 8) & 0xf;
	for(i = 0; i < 8; i++)
	{
		if(i == 0)
			glyph[1 + i * 2] = 0xfe;
		else
			glyph[1 + i * 2] = (font_glyph_unkown_0000_000f[idx][i] << 3) | 0x02;
	}

	idx = (code >> 4) & 0xf;
	for(i = 0; i < 8; i++)
	{
		if(i == 7)
			glyph[16 + i * 2] = 0xff;
		else
			glyph[16 + i * 2] = (font_glyph_unkown_0000_000f[idx][i + 1] << 1) | 0x80;
	}

	idx = (code >> 0) & 0xf;
	for(i = 0; i < 8; i++)
	{
		if(i == 7)
			glyph[17 + i * 2] = 0xfe;
		else
			glyph[17 + i * 2] = (font_glyph_unkown_0000_000f[idx][i + 1] << 3) | 0x02;
	}

	return (const uint8_t *)&glyph[0];
}

static const uint8_t * font_glyph_lookup(uint32_t code, int * width, int * height)
{
	const uint8_t * glyph;
	int w = 8, h = 16;

	switch(code)
	{
	case 0x2500:
		glyph = &font_glyph_2500[0];
		break;
	case 0x2502:
		glyph = &font_glyph_2502[0];
		break;

	case 0x250c:
		glyph = &font_glyph_250c[0];
		break;
	case 0x2510:
		glyph = &font_glyph_2510[0];
		break;
	case 0x2514:
		glyph = &font_glyph_2514[0];
		break;
	case 0x2518:
		glyph = &font_glyph_2518[0];
		break;

	case 0x25b2:
		glyph = &font_glyph_25b2[0];
		break;
	case 0x25b6:
		glyph = &font_glyph_25b6[0];
		break;
	case 0x25bc:
		glyph = &font_glyph_25bc[0];
		break;
	case 0x25c0:
		glyph = &font_glyph_25c0[0];
		break;

	default:
		if((code >= 0) && (code <= 0x7f))
		{
			glyph = &font_glyph_0000_007f[code][0];
		}
		else
		{
			glyph = font_glyph_unkown(code);
			w = 16;
		}
		break;
	}

	if(width)
		*width = w;
	if(height)
		*height = h;
	return glyph;
}

static int font_graph_draw(struct surface_t * s, const uint8_t * glyph, int x, int y, int w, int h, uint32_t color)
{
	uint32_t * dp;
	int i, j, k;
	int m, n, skip;

	dp = (uint32_t *)s->pixels + y * s->width + x;
	m = h;
	n = w / 8;
	skip = s->width - w;

	for(k = 0; k < m; k++)
	{
		for(j = 0; j < n; j++)
		{
			for(i = 0; i < 8; i++)
			{
				if(*glyph & (0x80 >> i))
					*dp = color;
				dp++;
			}
			glyph++;
		}
		dp += skip;
	}
	return w;
}

void font_draw(struct surface_t * s, struct region_t * clip, int x, int y, const char * utf8, struct color_t * c)
{
	struct region_t r, region;
	const uint8_t * glyph;
	const char * p;
	uint32_t code;
	uint32_t color;
	int w, h;

	region_init(&region, 0, 0, s->width, s->height);
	if(clip)
	{
		if(!region_intersect(&region, &region, clip))
			return;
	}
	color = color_get_premult(c);

	for(p = utf8; utf8_to_ucs4(&code, 1, p, -1, &p) > 0;)
	{
		glyph = font_glyph_lookup(code, &w, &h);
		region_init(&r, x, y, w, h);
		if(region_contains(&region, &r))
			font_graph_draw(s, glyph, x, y, w, h, color);
		x += w;
	}
}
